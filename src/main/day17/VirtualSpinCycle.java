package main.day17;

public class VirtualSpinCycle {
	
	private Integer lastValueAfterLastIndex;
	private Integer insertCount;
	private Integer valueAtOne;
	private Integer currentSizeOfList;
	private Integer currentIndex;
	private Integer stepCounter;

	
	public Integer getLastValueAfterLastIndex() {
		return lastValueAfterLastIndex;
	}
	public void setLastValueAfterLastIndex(Integer lastValueAfterLastIndex) {
		this.lastValueAfterLastIndex = lastValueAfterLastIndex;
	}
	public Integer getInsertCount() {
		return insertCount;
	}
	public void setInsertCount(Integer insertCount) {
		this.insertCount = insertCount;
	}
	public Integer getValueAtOne() {
		return valueAtOne;
	}
	public void setValueAtOne(Integer valueAtOne) {
		this.valueAtOne = valueAtOne;
	}
	public Integer getCurrentSizeOfList() {
		return currentSizeOfList;
	}
	public void setCurrentSizeOfList(Integer currentSizeOfList) {
		this.currentSizeOfList = currentSizeOfList;
	}
	public Integer getCurrentIndex() {
		return currentIndex;
	}
	public void setCurrentIndex(Integer currentIndex) {
		this.currentIndex = currentIndex;
	}
	public Integer getStepCounter() {
		return stepCounter;
	}
	public void setStepCounter(Integer stepCounter) {
		this.stepCounter = stepCounter;
	}


}
